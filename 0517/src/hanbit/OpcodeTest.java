package hanbit;

public class OpcodeTest {

		public static void main(String[] args) {
			
			int a = 20 , b = 7 , c = a+b ; //변수 초기화
			System.out.println("a+b="+c);
			
			c= a-b; 
			System.out.println("a-b="+c);
			
			c= a%b;
			System.out.println("a%b="+c);
			
//		%는 홀수짝수 구하거나 배수구할때 자주 사용
			
		}
}
